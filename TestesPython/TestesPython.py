#Testes com metodos nos prints
mensagem = "o grande teste"

print(mensagem.find("t"))    #Conta o número de caracteres até o valor parametizado
print(mensagem.count('a'))   #Conta quantos caracteres = ao valor parametizado existem na string
print(mensagem.replace("grande", "pequeno")) #Substitui uma parte da string (1° parametro) pelo valor do 2° parametro
print(mensagem.capitalize()) #Alera a 1° letra da string para maiuscula
print(mensagem.upper())      #Altera os caracteres da string para maiusculos
